                  <p> We encourage you to become a member of DBSA Metro Detroit.  A subscription to our newsletter, <em>Life In Balance</em>, comes with your membership.  The sales of <em>Life In Balance</em> make up most of DBSA Metro Detroit&rsquo;s income.  If finances are a concern, subscriptions are available at a reduced cost.</p>
                  <p class="SubHeads"><br />
                    Guidelines for DBSA support group members</p>
                  <ol>
                    <li>We respect and support other members of the group and encourage their ongoing attendance.</li>
                    <li>The essential principle of confidentiality is followed: <strong>&ldquo;What is said here stays here,&rdquo;</strong> unless there is an immediate safety issue- for example, if a person is suicidal or a danger to others.</li>
                    <li>One person talks at a time without interruptions. We agree not to monopolize group time or have side conversations.  Please respect other people&rsquo;s time.</li>
                    <li>No one is required to speak, it&rsquo;s o.k. to pass.</li>
                    <li>We speak about what is of concern to us, using &ldquo;I&rdquo; statements, sharing what we know and feel from our own personal experience.</li>
                    <li>We do not use profanity, insults, threatening or sexually explicit language or behavior.</li>
                    <li>No one is allowed to participate in a group under the influence of alcohol or illicit drugs.</li>
                    <li>We give no advice on specific medication or diagnoses. We know we are all different and these are issues between you and your mental health professionals.</li>
                    <li>We give positive feedback whenever possible after membersshare.</li>
                    <li>Differences of opinion are o.k. We strive to be non-judgmental and learn from others&rsquo; experiences.</li>
                    <li>It is everyone&rsquo;s responsibility to make the discussion group a safe place to share.</li>
                    </ol>
