                  <p> Welcome to the Depression and Bipolar Support Alliance Metro Detroit.  We are a peer-run and all volunteer non-profit organization.  Our meetings offer individuals challenged with depressive disorders an open and sympathetic atmosphere to talk with one another.  Family and friends are also welcome.</p>
                  <p> Remember, <strong>&ldquo;What is said here, stays here.&rdquo;</strong>  Exceptions to confidentiality will occur when and if someone&rsquo;s safety is in question.</p>
