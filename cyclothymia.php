                  <p class="BodyText"><span class="SubHeads">Definition</span><br />
                    Cyclothymia (si-kloh-THIGH-me-uh), also called cyclothymic disorder, is a mild form of bipolar disorder. Like bipolar disorder, cyclothymia is a chronic mood disorder that causes emotional ups and downs. &nbsp;With cyclothymia, you experience periods when your mood noticeably fluctuates from your baseline. You may feel on top of the world for a time, followed by a low period when you feel somewhat blue. Between these cyclothymic highs and lows, you may feel stable and fine. Compared with bipolar disorder, the highs and lows of cyclothymia are less extreme. Still, it's critical to seek help managing these symptoms because they increase your risk of bipolar disorder. Treatment options for cyclothymia include psychotherapy, medications, and &mdash; most important &mdash; close, ongoing follow-up with your doctor..</p>
                  <p class="BodyText"><span class="SubHeads">Symptoms</span><br />
                    Cyclothymia symptoms include an alternating pattern of emotional highs and lows. The highs of cyclothymia are characterized hypomanic symptoms, which resemble those of mania, but are less severe. The lows consist of mild or moderate depressive symptoms. <br />
                    Cyclothymia symptoms are generally similar to those of bipolar disorder, but they're less severe. When you have cyclothymia, you can typically function in your daily life, though not always well. The unpredictable nature of your mood shifts may significantly disrupt your life because you never know how you're going to feel &mdash; and you can't just will yourself to live life on an even keel.</p>
                  <p class="BodyText"><span class="SubHeads">Hypomanic phase of cyclothymic disorder<br />
                  </span>Signs and symptoms of hypomanic episodes of cyclothymia may include:<br />
                  &bull; Unusually good mood or cheerfulness (euphoria) <br />
                  &bull;	Extreme optimism <br />
                  &bull;	Inflated self-esteem <br />
                  &bull;	Poor judgment <br />
                  &bull;	Rapid speech <br />
                  &bull;	Racing thoughts <br />
                  &bull;	Aggressive or hostile behavior <br />
                  &bull;	Being inconsiderate of others <br />
                  &bull;	Agitation <br />
                  &bull;	Increased physical activity <br />
                  &bull;	Risky behavior <br />
                  &bull;	Spending sprees <br />
                  &bull;	Increased drive to perform or achieve goals <br />
                  &bull;	Increased sexual drive <br />
                  &bull;	Decreased need for sleep <br />
                  &bull;	Tendency to be easily distracted <br />
                  &bull;	Inability to concentrate</p>
                  <p class="BodyText"><span class="SubHeads">Depressive phase of cyclothymic disorder</span><br />
                    Signs and symptoms of depressive episodes of cyclothymia may include:<br />
                    &bull;	Sadness <br />
                    &bull;	Hopelessness <br />
                    &bull;	Suicidal thoughts or behavior <br />
                    &bull;	Anxiety <br />
                    &bull;	Guilt <br />
                    &bull;	Sleep problems <br />
                    &bull;	Appetite problems <br />
                    &bull;	Fatigue <br />
                    &bull;	Loss of interest in daily activities <br />
                    &bull;	Decreased sex drive <br />
                    &bull;	Problems concentrating <br />
                    &bull;	Irritability <br />
                    &bull;	Chronic pain without a known cause </p>
                  <p class="BodyText"><span class="SubHeads">Causes</span><br />
It's not known specifically what causes cyclothymia. As with many mental disorders, research shows that it may result from a combination of:<br />
&bull;	Heredity &mdash; cyclothymia tends to run in families <br />
&bull;	Your body's biochemical processes, such as changes in brain chemistry <br />
&bull;	Environment </p>
                  <p class="BodyText"><span class="SubHeads">Risk factors</span><br />
                    Cyclothymia is thought to be relatively rare. But true estimates are hard to pin down because people may be undiagnosed or misdiagnosed as having other mood disorders, such as depression. </p>
                  <p class="BodyText">Cyclothymia typically starts during adolescence or young adulthood. The condition affects about the same number of men and women.</p>
                  <p class="BodyText"><span class="SubHeads">Diagnostic Criteria for Cyclothymic Disorder</span><br />
                  </p>
                  <p class="BodyText">&bull;	For at least 2 years, the presence of numerous periods of hypomanic symptoms and numerous periods of depressive symptoms that do not meet criteria for a Major Depressive Episode. Note: In children and adolescents, the duration must be at least 1 year.</p>
                  <p class="BodyText">&bull;	During the above 2-year period (1 year for children or adolescents), the person has not been without the symptoms in Criterion A for more than 2 months at a time.</p>
                  <p class="BodyText">&bull;	No Major Depressive Episode , Manic Episode, or Mixed Episode has been present during the first 2 years of the disturbance.</p>
                  <p class="MeetingText">Note:  After the initial 2 years (1 year in children or adolescents) of Cyclothymic Disorder, there may be superimposed Manic or Mixed Episodes (in which case both Bipolar I Disorder and Cyclothymic Disorder may be diagnosed) or Major Depressive Episodes (in which case both Bipolar II Disorder and Cyclothymic Disorder may be diagnosed).</p>
                  <p class="BodyText">&bull;	The symptoms in Criterion A are not better accounted for by Schizoaffective Disorder and are not superimposed on Schizophrenia, Schizophreniform Disorder, Delusional Disorder, or Psychotic Disorder Not Otherwise Specified.</p>
                  <p class="BodyText">&bull;	The symptoms are not due to the direct physiological effects of a substance (e.g., a drug of abuse, a medication) or a general medical condition (e.g., hypothyroidism).</p>
                  <p class="BodyText">&bull;	The symptoms cause clinically significant distress or impairment in social, occupational, or other important areas of functioning.</p>
