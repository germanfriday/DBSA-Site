                  <p class="BodyText"><span class="SubHeads">Diagnostic Features</span><br />
                    Dysthymic Disorder is a chronic condition characterized by depressive symptoms that occur for most of the day, more days than not, for at least 2 years. In children, the mood may be irritable rather than depressed, and the required minimum duration is only 1 year. During this 2-year period (1 year for children or adolescents), any symptom-free interval can not last longer than 2 months. By definition, this diagnosis is not made if there are any Hypomanic, Manic or Mixed Episodes. This disorder's depressive symptoms are not due to a medical condition, medication, illegal drug, or Psychotic Disorder. In the first 2 years of this disorder, if the depressive symptoms intensify to meet the full criteria for a Major Depressive Episode; the diagnosis would be changed to Major Depressive Disorder. It is common for an individual to have 2 or more years of Dysthymic Disorder, then later develop a Major Depressive Episode. In such cases (&quot;double depression&quot;), both Major Depressive Disorder and Dysthymic Disorder are diagnosed. Once the Major Depressive Episode disappears, but the dysthymic symptoms persist, only Dysthymic Disorder is diagnosed. </p>
                  <p class="BodyText"><span class="SubHeads">Complications</span><br />
                    By definition, there must clinically significant distress or impairment in social, occupational, or other important functioning as result of the mood disturbance. In childhood, this disorder is often associated with impaired school performance and poor social interaction. Children and adolescents with this disorder are usually irritable and cranky as well as depressed. They have low self-esteem, poor social skills, and are pessimistic.</p>
                  <p class="BodyText"><span class="SubHeads">Comorbidity</span><br />
In adults, this disorder is associated with an increased risk of having Major Depressive Disorder and Substance-Related Disorders. In children, this disorder is associated with an increased risk of having Attention-Deficit/Hyperactivity Disorder, Conduct Disorder, Anxiety Disorders, Learning Disorders, and Mental Retardation.</p>
                  <p class="BodyText"><span class="SubHeads">Associated Laboratory Findings</span><br />
                    No laboratory test has been found to be diagnostic of this disorder. Sleep EEG abnormalities are evident in 25%-50% of adults with this disorder. These are the same EEG sleep abnormalities that are found in Major Depressive Disorder (e.g., reduced rapid eye movement [REM] latency, increased REM density, reduced slow-wave sleep, impaired sleep continuity). Dexamethasone nonsuppression (which often occurs in Major Depressive Disorder) is not common in Dysthymic Disorder (unless it co-exists with Major Depressive Disorder). </p>
                  <p class="BodyText"><span class="SubHeads">Prevalence<br />
                  </span>Lifetime prevalence for this disorder in the general population is 6%. In any year, 3% of the general population has this disorder. In adulthood, women are 2-3 times more likely to develop this disorder than men. </p>
                  <p><span class="SubHeads">Course</span><br />
                    <span class="BodyText">This chronic disorder usually has an early and insidious onset in childhood or adolescence. In adults, up to 75% of individuals with this disorder will develop Major Depressive Disorder within 5 years. The spontaneous recovery rate for this disorder is approximately 10% per year. This recovery rate is significantly better with active treatment.</span></p>
                  <p><span class="SubHeads">Familial Pattern</span><br />
                    <span class="BodyText">First-degree biological relatives of individuals with disorder have elevated rates of Dysthymic Disorder and Major Depressive Disorder compared with the general population. Dysthymic Disorder is more common in the first-degree biological relatives of individuals with Major Depressive Disorder.</span></p>
                  <p><span class="SubHeads">Treatment</span><br />
                    <span class="BodyText">There is little research on the treatment of Dysthymic Disorder. Generally, research is showing that the medications that are effective in treating Major Depressive Disorder are also effective in Dysthymic Disorder. Individuals with this disorder respond to tricyclic antidepressants, MAOI antidepressants (classical and reversible), and SSRI antidepressants (of which the best current evidence is for fluoxetine). Supportive psychotherapy and psychoeducation (teaching patients and their families about this illness) significantly improve patient compliance and family cooperation.</span></p>
                  <p><span class="SubHeads">Symptoms</span><br />
                    <span class="BodyText">Dysthymia symptoms in adults include: <br />
&bull;	Loss of interest in daily activities <br />
&bull;	Feeling sad or down <br />
&bull;	Hopelessness <br />
&bull;	Lack of energy <br />
&bull;	Fatigue <br />
&bull;	Low self-esteem <br />
&bull;	Trouble concentrating <br />
&bull;	Trouble making decisions <br />
&bull;	Self-criticism <br />
&bull;	Excessive anger <br />
&bull;	Decreased productivity <br />
&bull;	Avoiding social activities <br />
&bull;	Feelings of guilt <br />
&bull;	Poor appetite <br />
&bull;	Overeating <br />
&bull;	Sleep problems <br />
In children,dysthymia symptoms may include: <br />
&bull;	Irritability <br />
&bull;	Poor school performance <br />
&bull;	A pessimistic attitude <br />
&bull;	A lack of social skills <br />
Dysthymia symptoms typically come and go over a period of years, and their intensity can change over time, too. But in general, you may find it hard to be upbeat even on happy occasions &mdash; you may be characterized as having a gloomy personality. <br />
When dysthymia starts on or before age 21, it's called early-onset dysthymia. When it starts after that, it's called late-onset dysthymia.</span></p>
                  <p><span class="SubHeads">Diagnostic Criteria for Dysthymia</span><br />
                    <span class="BodyText">To be diagnosed with dysthymia, you must meet the symptom criteria spelled out in the American Psychiatric Association's Diagnostic and Statistical Manual of Mental Disorders (DSM). <br />
                    Main criterion<br />
                    The main criterion to be diagnosed with dysthymia is: <br />
&bull;	Having a depressed mood most of the time for two or more years (in children, the duration must be at least one year, and their mood can be irritable rather than depressed). <br />
Additional criteria<br />
In addition to that, you must have at least two of these symptoms, and they must cause you distress or interfere with your ability to function in your daily life: <br />
&bull;	Poor appetite or overeating <br />
&bull;	Sleep problems <br />
&bull;	Fatigue or lack of energy <br />
&bull;	Low self-esteem <br />
&bull;	Hopelessness <br />
&bull;	Poor concentration <br />
&bull;	Trouble making decisions <br />
&bull;	Make sure you understand if you have been diagnosed with dysthymia or another condition so you can learn more about your specific situation and get appropriate treatment.</span></p>
                  <p><span class="SubHeads">Prevention</span><br />
                    <span class="BodyText">There's no sure way to prevent dysthymia. Because dysthymia often starts in childhood, identifying children at risk of the condition may be of some benefit, though, by encouraging early treatment. Also, taking steps to control stress, to increase your resilience and to boost low self-esteem may help ward off dysthymia symptoms. Friendship and social support, especially in times of crisis, can help you weather rough spells. Also, treatment at the earliest sign of a problem can help prevent dysthymia from worsening. Long-term maintenance treatment also may help prevent a relapse of dysthymia symptoms. </span></p>
