                <p align="left" class="BodyText"><br />
                  <span class="BodyTextBold">An Example of Mental Illness </span><br />
                  My motto
                  is &quot;If someone else can do it so can I.&quot; Through
                  positive and monitored negative examples, the struggles mental
                  illness causes in a person's life can actually bring HOPE to
                  his/her future. A positive example can change a mentally ill
                  person's track of life. Not all mentally challenged people
                  have to end up on disability and destitute. There are exceptions
                  to all society's rules. Positive examples can lead a person
                  to a road of HOPE, CHALLENGE and SUCCESS.</p>
                <p align="left" class="BodyText"> I consider myself a positive example
                    of a woman challenged with mental illness who persisted for
                    many years to get a college degree. Each step taken on the
                    ladder of my life has been a struggle, but I always persisted
                    through any obstacle, including my mental illness. I don't
                    believe in words like &quot;no,
                  can't, won't, never and quit&quot; when it comes to learning.
                  Learning always came hard for me, but I always knew I could
                  do it. I was called a slow learner. In 1983, I graduated from
                  high school with a 2.0 GPA and was told by my counselor that
                  I was not college material and to get a job at McDonald's.
                  Needless to say I didn't listen.</p>
                <p align="left" class="BodyText"> I was bound and determined that I was going to find a university
                  to accept me into a bachelors program. After getting turned
                  down from six universities in the area, I was finally accepted
                  into Wayne State University, College of Lifelong Learning,
                  Interdisciplinary Studies Program. My life consisted of a part-time
                  job, regulated medication and studying for school. I graduated
                  in 1994 with a 3.2 GPA.</p>
                <p align="left"><span class="BodyText"> When I think of the future, I think
                    of myself dreaming. I always feel my dreams will come true,
                    but my pathways are never the same way I dream. When will
                    I get my PhD? When I'm 45, 50 or 55 - who knows? I always
                    thought I'd get my bachelors in five years, but I got it
                    in ten years. I am currently working on my masters degree.
                    After I get my masters, I want to become a lobbyist. I feel
                    I can be a role model to those diagnosed with mental illness.
                    The fact that I am bipolar will emphasize my motto - &quot;If someone else can do it so can I.&quot; I
                  feel as each year goes by and I remain in remission I grow
                  stronger. My last episode was May 1994.</span> </p>
                <p align="left" class="Signoff"> Katherine B.<br />
  Facilitator DBSA Women Only Group</p>
